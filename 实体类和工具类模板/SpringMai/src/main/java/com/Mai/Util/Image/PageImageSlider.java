package com.Mai.Util.Image;

import java.awt.image.BufferedImage;

public class PageImageSlider {
	BufferedImage smliBuff;
	BufferedImage begBuff;
	int width;
	int height;
	public BufferedImage getSmliBuff() {
		return smliBuff;
	}
	public void setSmliBuff(BufferedImage smliBuff) {
		this.smliBuff = smliBuff;
	}
	public BufferedImage getBegBuff() {
		return begBuff;
	}
	public void setBegBuff(BufferedImage begBuff) {
		this.begBuff = begBuff;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
	
	
	
}
